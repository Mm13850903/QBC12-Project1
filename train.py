class Train:
    def __init__(self, train_id, name, line_name, speed, stop_time, quality, price, capacity):
        self.train_id = train_id
        self.name = name
        self.line_name = line_name
        self.speed = speed
        self.stop_time = stop_time
        self.quality = quality
        self.price = price
        self.capacity = capacity